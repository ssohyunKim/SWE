
public class TodoList {
	String date;
	String due;
	String description;
	
	public String getDate() {
		return date;
	}
	
	public void setDate() {
		this.date = date;
	}
	
	public String getDue() {
		return due;
	}
	
	public void setDue() {
		this.due = due;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription() {
		this.description = description;
	}
}
